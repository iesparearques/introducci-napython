# Operaciones con Enteros

entero1 = 1
entero2 = 2

print("La suma entera de ", entero1, "+",entero2,"es",entero1 + entero2)
print("La resta entera de ", entero1, "-",entero2,"es",entero1 - entero2)
print("La multiplicación entera de ", entero1, "*",entero2,"es",entero1 * entero2)
print("La división entera de ", entero1, "//",entero2,"es",entero1 // entero2)
print("El resto de la división entera de ", entero1, "%",entero2,"es",entero1 % entero2)

